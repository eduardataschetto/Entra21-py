# Crie um program que peça ao usuário que digite 2 números reias(float) e mostre:
# 
# A soma dos dois
# A multiplicação dos dois
# A divisão do primeiro com o segundo
# A subtração dos dois

n1 = float(input("Informe o primeiro número: "))
n2 = float(input("Informe o segundo número: "))

print("A soma entre os número informados  é "+str(n1+n2))
print("A multiplicação entre os número informados  é "+str(n1*n2))
print("A divisão entre os número informados  é "+str(n1/n2))
print("A subtração entre os número informados  é "+str(n1-n2))